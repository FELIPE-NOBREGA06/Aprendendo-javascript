// EXERCÍCIO 30: Nível de Inglês
// 0-25: Básico, 26-50: Intermediário, 51-75: Avançado, 76-100: Fluente

// Seu código aqui:

let pontuacao = 60;

if (pontuacao >= 0 && pontuacao <= 25) {
  console.log("Básico");
} else if (pontuacao >= 26 && pontuacao <= 50) {
  console.log("Intermediário");
} else if (pontuacao >= 51 && pontuacao <= 75) {
  console.log("Avançado");
} else if (pontuacao >= 76 && pontuacao <= 100) {
  console.log("Fluente");
}
