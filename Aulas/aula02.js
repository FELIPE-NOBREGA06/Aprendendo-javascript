// ===== AULA 02: VARIÁVEIS E CONCATENAÇÃO =====

// USE STRICT
// "use strict" ativa o modo estrito do JavaScript
// Ajuda a evitar erros comuns e código inseguro
"use strict";

// DECLARANDO VARIÁVEIS
// let cria uma variável que pode ter seu valor alterado
// Sintaxe: let nomeDaVariavel = valor;
let nome = "felipe";

// EXIBINDO VARIÁVEIS
// Podemos exibir o valor de uma variável com console.log()
console.log(nome); // Exibe: felipe

// CONCATENAÇÃO
// O operador + une (concatena) strings
// Podemos misturar texto fixo com variáveis
console.log("Nome: " + nome); // Exibe: Nome: felipe
