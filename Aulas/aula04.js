// ===== AULA 04: OPERADORES ARITMÉTICOS =====

// DECLARAÇÃO MÚLTIPLA
// Podemos declarar várias variáveis em uma linha
// Separadas por vírgula
let num1 = 0,
  num2 = 0,
  res = 0;

// ATRIBUINDO VALORES
num1 = 5;
num2 = 10;

// OPERADOR MÓDULO (%)
// O operador % retorna o RESTO da divisão
// Exemplos:
// 5 % 10 = 5 (5 dividido por 10 = 0, resto 5)
// 10 % 5 = 0 (10 dividido por 5 = 2, resto 0)
// 7 % 3 = 1 (7 dividido por 3 = 2, resto 1)
res = num1 % num2;

// EXIBINDO O RESULTADO
console.log(res); // Exibe: 5

